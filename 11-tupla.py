filmsTuple = ("Batman", "Superman", "One Piece", "Naruto", "Pulp Fiction")
print(type(filmsTuple))

# 1- Buscar os dois primeiros itens da tupla
print(filmsTuple[:2])

# 2- Buscar o ultimo item da tupla
print(filmsTuple[-1])

# 3- Buscar filmes até uma determinada posição
print(filmsTuple[:3])

# 4- Buscar filmes de uma posição em diante
print(filmsTuple[2:])

# 5- Recuprar um item da tupla pelo nome
print(filmsTuple.index("Naruto"))